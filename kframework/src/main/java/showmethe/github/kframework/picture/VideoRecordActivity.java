    private static  final int PERMISSION_CODE =  5001;

    private String[] permissions = {
            Manifest.permission.RECORD_AUDIO,
            Manifest.permission.CAMERA
    };
    private List<String> needPermissions = new ArrayList<>();


    private void onPermission(){
        for(String per : permissions){
            if(ContextCompat.checkSelfPermission(this,per) != PackageManager.PERMISSION_GRANTED){
                needPermissions.add(per);
            }
        }
        if(needPermissions.size()>0){
            ActivityCompat.requestPermissions(this,needPermissions.toArray(new String[0]),PERMISSION_CODE);
        }
    }

    @Override
    public void onRequestPermissionsResult(int requestCode, @NonNull String[] permissions, @NonNull int[] grantResults) {
        switch (requestCode){
            case PERMISSION_CODE:
                for(String per : needPermissions){
                    if(ContextCompat.checkSelfPermission(this,per) == PackageManager.PERMISSION_GRANTED){
                        Toast.makeText(this, "申请成功", Toast.LENGTH_SHORT).show();
                    }
                }
                break;

        }
    }

